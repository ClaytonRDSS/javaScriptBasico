// Operadores e expressões Aritimeticas.

console.log(7 + 8) // (Adição)
console.log(5.2 - 3) // (Subtração) 
console.log(2 * 9) // (Multiplicação)
console.log(9 / 2) // (Divisão)
console.log(9 % 3) // (Resto da Divisão = 9 / 3 = 3 -> sobra =  0(resto))
console.log(9 % 4) // (Resto da Divisão = 9 / 4 = 2 -> sobra =  1(resto))
console.log(2 ** 3) // (Potencia = 2x2x2 = 8)